// Operadores de Atribuição em JavaScript (Node.js)

// Atribuição básica
let x = 10;
console.log(`Valor inicial de x: ${x}`);

// Operador de atribuição de adição (+=)
x += 5;  // Equivalente a: x = x + 5
console.log(`Após x += 5 (adição): ${x}`);

// Operador de atribuição de subtração (-=)
x -= 3;  // Equivalente a: x = x - 3
console.log(`Após x -= 3 (subtração): ${x}`);

// Operador de atribuição de multiplicação (*=)
x *= 2;  // Equivalente a: x = x * 2
console.log(`Após x *= 2 (multiplicação): ${x}`);

// Operador de atribuição de divisão (/=)
x /= 4;  // Equivalente a: x = x / 4
console.log(`Após x /= 4 (divisão): ${x}`);

// Operador de atribuição de módulo (%=) - resto da divisão
x %= 5;  // Equivalente a: x = x % 5
console.log(`Após x %= 5 (módulo): ${x}`);

// Operador de atribuição de exponenciação (**=)
x **= 3; // Equivalente a: x = x ** 3
console.log(`Após x **= 3 (exponenciação): ${x}`);

// Bônus: Operador de atribuição de concatenação (com strings)
let texto = "Node";
texto += ".js";  // Concatenação de strings
console.log(`\nExemplo com strings: ${texto}`);
