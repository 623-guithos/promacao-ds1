// Crie uma variável para armazenar um número inteiro
let numero = -5; // Experimente mudar para 10, 0, -5, etc.

// Use estruturas condicionais para verificar o número
if (numero > 0) {
  console.log("O número é positivo.");
} else if (numero < 0) {
  console.log("O número é negativo.");
} else {
  // Se não for maior que 0 e nem menor que 0, só pode ser 0
  console.log("O número é zero.");
}
